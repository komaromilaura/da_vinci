                <h2>Élete</h2>
                <p><strong> 1452. április 15-én született Vinciben,</strong> egy középkori vár és a Montalbano hegyei által övezett kis falucskában, Ser Piero di Antonio jegyző fiaként. Szinte semmit nem tudni korai éveiről. Még szülőfalujában sincs írásos nyoma tehetségének és esetleges inaskodásának azt megelőzően, hogy 1469-ben Verrocchio firenzei műhelyéhez csatlakozott volna. Azt sem tudni, hogy legkorábbi, 1473-as rajza előtt készített-e másokat is.</p>

                <p>Leonardo da Vinci karrierje ezt követően kezdett felívelni, hamarosan már Velence, Milánó, Firenze, Róma és Franciaország felkérésére dolgozott.</p>

                <p>1478-ban megfestette a Madonna Benoisként ismert képet, két évvel később már a Mediciek szolgálatába állt. Ezután 2-3 évente változtatta helyét, <strong>dolgozott a Sforza családnak, Cesare Borgiának, I. Ferenc francia királynak, de több Medicinek is.</strong> Közben olyan művek kerültek ki a keze alól, mint az Angyali üdvözlet (1478), Szent Jeromos (1480–82), Sziklás Madonna (1483–86), A hermelines nő (1488–90), a Zenész képmása (1490) vagy a mai napig használt, az emberi test arányait tökéletesen ábrázoló Vitruvianus (1490). 1495-ben kezdte meg az Utolsó vacsora munkálatait, Milánó hercegének felkérésére. Közel 3 évig készítette a művet, többtucatnyi előtanulmányt rajzolva, figyelembe véve a terem fényviszonyait is. Az eredmény egyedülálló: nem csupán újszerű technikájával, de vallástörténeti jelentőségű ábrázolásával is azonnal hírnevet vívott ki művének. 1503-ban felkérték az Anghiari csata megfestésére, amelyhez hasonló komoly előkészítési munkálatokkal látott neki, mint az Utolsó vacsora esetében tette, de soha nem fejezte be művét. Ekkor kezdte festeni leghíresebb művét, amelyet egészen haláláig alakított, javítgatott: a Mona Lisát. Ebből a korszakból való még a Léda és a hattyú (1506–08), a Szent Anna harmadmagával (1510), a Keresztelő Szent János (1513–16) című festménye, valamint Önarckép (1515) néven elhíresült rajza is.</p>

                <p><strong>1517-ben Amboise-ba, I. Ferenc udvarába költözött, ahol a király első festője, mérnöke és építésze címen említik a feljegyzések.</strong></p>

                <p>Leonardo szinte egész életében kéziratokat és jegyzetfüzeteket töltött meg feljegyzésekkel és rajzokkal, gondolataival, elképzeléseivel, sőt személyes dolgaival is. Ezekből a jegyzetekből ismerte meg az utókor találmányait, mechanikai, matematikai, asztronómiai, fizikai, földrajzi, botanikai, kémiai és anatómiai feljegyzéseit, festészeti és szobrászati elméleti és gyakorlati megfigyeléseit, fény-árnyék elméletét, optikai és perspektivikus felfedezéseit. Ezekből a jegyzeteiből bizonyítható, hogy melyik művét mikor adta át a megrendelőnek, és mikor kezdett vele foglalkozni, hogy rendszeresen boncolt hullákat, tökéletesítve anatómiai ismereteit.</p>

                <p>Leonardo da Vinci <strong>1519. május 2-án, 67 éves korában halt meg</strong> Franciaországban.</p>

                <p>Az átlagos életkor 28 év volt a XV. században, így könnyen kiszámítható, hogy Leonardo da Vinci mai szemmel megközelítőleg 120 éves aggastyánként távozott az élők sorából.</p>
