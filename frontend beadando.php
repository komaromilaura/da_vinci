                <h2>Leonardo, a tudós</h2>
                
                <p>Szülőfalujában a művészet kedvéért behatóan tanulmányozta a természeti környezetet. Számos értékes megfigyelése alapján <u>több tudományág is legjelentősebb előfutárai között tartja számon.</u> Így például felismerte a földkéreg függőleges mozgásait és azt, hogy a földtörténeti múlt számos eseményére következtethetünk az ősmaradványok vizsgálatából. <em>„Ami valamikor tengerfenék volt, most hegycsúcsként emelkedik a magasba</em> – írta. – <em>Erről tanúskodnak a tengeri kagylóhéjak, osztrigák és korallok, amelyeket most a hegyek csúcsán találunk.”</em></p>

                <p>Milánóban a művészi emberábrázolás tökéletesítésének szándékával fogott bele a tudományos igényű boncolásba. Látta, hogy az emberi viselkedés hűséges ábrázolásához nélkülözhetetlen annak a belső életnek a teljes ismerete, ami alapvetően meghatározza a külső megjelenést.</p>

                <p>A perspektíváról, festészetről alkotott tudományos írásait nem gyűjtötte össze, mint ahogy a filozófiáról, az anatómiáról szóló munkáinak kiadása is csak terv maradt.</p>

                <p>Másfelől kidolgozott egy új, a hagyományos vallási előírásokat is kielégítő jelképrendszert: hogy az Atyára mindig valamely szent mutat rá, aki Őt láthatja. A Szentlélek galamb ábrázolása helyett pedig szintén valamely szentet mutat be, akit a Biblia szerint „betöltött”. Emiatt látható az Utolsó vacsorán Jézus jobbján Szent Erzsébet, akit a Szentlélek elsőként töltött be, és most tudatában fiának, Keresztelő Jánosnak már bekövetkezett, valamint Jézus várható szomorú sorsában- behunyt szemmel zokog. Szent Erzsébet, Jézus, és közöttük a pillér – mint az Atya ősi jelképe a Szentháromság máig ismeretlen, meg nem értett megjelenítése.</p>

                <p>További felfedezése a szellemi és a valós világok különválasztása – a szellemi világban a szenteket pl. nem jelöli glóriával. Számos más, érdekes megoldást alkalmazott pl. az idő körforgásának bemutatására, de ezt meg nem értve, el nem fogadva elfelejtették.</p>

                <h2>Anatómiai munkássága</h2>

                <p><u>Vitruvius-tanulmány</u> <em>„Az ember kinyújtott karjainak hossza megegyezik a magasságával. A haja tövétől az álla hegyéig terjedő szakasz egytizede a magasságnak; az álla hegyétől a feje tetejéig terjedő szakasz egynyolcada a magasságának; a mellkasa tetejétől a haja tövéig egyhetede az egész embernek.”</em> Az emberi test a művészetekre tett hatását és arányait a Vitruvius-tanulmány munkájában foglalta össze. Továbbá az emberi test felépítésének feltárásában is úttörő szerepe volt. Gyakran végzett – akkoriban az egyház által tiltott – boncolásokat is.</p>
