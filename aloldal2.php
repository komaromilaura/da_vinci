            <p>Da Vinci 400 évvel azelőtt álmodott meg egy helikoptert, hogy azok ténylegesen léteztek volna. Most a mérnökök életre keltették a tervét, de egy kis csavarral.</p>

            <p>Több mint öt évszázaddal ezelőtt az olasz zseni, Leonardo da Vinci elkészítette egy helikopter tervrajzát. A koncepció ikonikusnak számít. Egyértelműen egy lebegő eszközt ábrázol, 400 évvel azelőtt, hogy az ilyen járművek megjelentek volna. Az biztos, hogy ez nem az általunk ismert helikopter, amelyet a repülőgépek szárnyaként működő lapos rotorlapátok hajtanak. A híres vázlat egy csavarszerű függőleges lapátot ír le, amely spirálisan forog a központi tengelye körül, a DNS-molekulák által híressé tett spirál alakjának egy változata. Gondoljunk a hajócsavar vízre gyakorolt hatására. A lapátok forgása eltolja a vizet, és Isaac Newton harmadik törvénye leírja a hajóra ható előre irányuló nyomást.</p>

            <p>Hasonlóképpen, Leonardo légcsavarja lefelé nyomja a levegőt, felfelé hajtva a járművét. Ha a csavar forgása elég gyors ahhoz, hogy a hajónál nagyobb tömegű levegőt kiszorítson, akkor a jármű felemelkedik a levegőben. A konstrukció alapvetően szilárd. Ami da Vincinek hiányzott, azok a modern anyagok, amelyek egy könnyű és tartós lapát megépítéséhez szükségesek. A spirálcsavart úgy írta le, mintha vászonból készült volna, amelynek pórusait keményítővel tömítették volna el. Emellett hiányzott a folyamatos hajtóerő is egy ilyen géphez. A forgattyúkat forgató emberek álmukban sem álmodhattak repülésről: túlságosan nehezek és túl gyengék ahhoz, hogy elegendő erőt termeljenek ahhoz, hogy felemeljék magukat.</p>

            <p>Ennek a korlátnak a tudatában da Vinci egy olyan rugót képzelt el, amelyet a forgattyúforgatók tekercselnek, és amely energiát gyűjt és tárol. Ezt a felgyülemlett energiát egy gyors kitekercseléssel lehetne felszabadítani, megpörgetve a csavarrotort. De tudomásunk szerint ilyen szerkezetet soha nem építettek. 2020-ban egy mérnökhallgatókból álló csapat nagyon részletes tanulmányt készített az aerodinamikáról és más gyakorlati tényezőkről, amelyek szükségesek ahhoz, hogy da Vinci helikoptere felemelkedjen. Megállapították, hogy a csavarrendszert számos módosítással egy modern drónba is be lehetne építeni, amely valóban repülhetne.</p>

            <p>A függőleges légcsavartengely, amelyhez a gerendák rögzülnek, alumíniumból készült, amely képes a legjobb kompromisszumot fenntartani a csőátmérő, a csőszélesség, a szilárdság és a gerendák rögzítésének képessége között. A mérnökök olyan vad módosításokat is javasoltak, mint a szénszálas karokra felfüggesztett apró volfrámsúlyok, amelyeket úgy hangoltak, hogy rezonáljanak a csavarban rejlő rezgési módokkal, és elnyeljék azokat. A mérnökök a tesztrepüléshez egy drón légcsavarjait piros műanyag csavarlapátokra cserélték, amelyeket a da Vinci tervei alapján végzett számításaik szerint készítettek. És működött a dolog.</p>
